from scipy.stats import pearsonr, spearmanr

x1 = [12.5, 15.3, 23.2, 26.4, 33.5, 34.4, 39.4, 45.2, 55.4, 60.9]
x2 = [21.2, 23.9, 32.9, 34.1, 42.5, 43.2, 49.0, 52.8, 59.4, 63.5]

# 皮尔逊相关系数演示
ret = pearsonr(x1, x2)  # 皮尔逊相关系数
print("皮尔逊相关系数为：", ret)  # 第一个参数是返回值，越接近|1|，相关新越强。第二个参数的意义是：如果两个数相关性越强，这个数越小。当样本数大于500，该参数更合理

# 斯皮尔曼相关系数演示
ret = spearmanr(x1, x2)
print("斯皮尔曼相关系数为：", ret)
